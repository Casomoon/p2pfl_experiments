    def to_p2p_modules(self, train_sets: list[list[dict]], val_sets: list[list[dict]], test_set: list[dict], batch_size: int = 8)-> list[P2PFLDataset]:
        assert len(train_sets) == len(val_sets)
        p2p_modules: list[P2PFLDataset] = []
        global_test_dataset = Dataset.from_list(test_set)

        for i in range(len(train_sets)): 
            client_train_dataset = Dataset.from_list(train_sets[i])
            client_val_dataset = Dataset.from_list(val_sets[i])
            client_p2p_dataset = P2PFLDataset(
                data = DatasetDict({
                    "train": client_train_dataset,
                    "validation": client_val_dataset,
                    "test": global_test_dataset
                }),
                train_split_name="train",
                #validation_split_name="valid", 
                test_split_name="test",
                transforms=None
            )
            p2p_modules.append(client_p2p_dataset)
        return p2p_modules